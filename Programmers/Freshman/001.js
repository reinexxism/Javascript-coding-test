// 001. 두 수의 합

function solution(num1, num2) {
  return num1 + num2;
}

// 002. 두 수의 차

function solution(num1, num2) {
  return num1 - num2;
}

// 003. 두 수의 곱

function solution(num1, num2) {
  return num1 * num2;
}

// 004. 몫 구하기

function solution(num1, num2) {
  return parseInt(num1 / num2);
}

// 005. 두 수의 나눗셈

function solution(num1, num2) {
  return parseInt((num1 / num2) * 1000);
}

